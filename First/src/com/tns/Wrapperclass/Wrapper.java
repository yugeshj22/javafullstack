package com.tns.Wrapperclass;

public class Wrapper {
	public void B(Boolean val) {
		System.out.println("Boolean:"+val);
	}
	public void C(Character val) {
		System.out.println("Character:"+val);
	}
	public void S(Short val) {
		System.out.println("Short:"+val);
	}
	public void By(Byte val) {
		System.out.println("Byte:"+val);
	}
	public void I(Integer val) {
		System.out.println("Integer:"+val);
	}
	public void L(Long val) {
		System.out.println("Long:"+val);
	}
	public void F(Float val) {
		System.out.println("Float:"+val);
	}
	public void D(Double val) {
		System.out.println("Double:"+val);
	}
}
